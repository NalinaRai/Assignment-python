# Taking input of grades for three exams
exam1_grade = float(input("Enter grade for exam 1: "))
exam2_grade = float(input("Enter grade for exam 2: "))
exam3_grade = float(input("Enter grade for exam 3: "))

# Calculating the average grade
average_grade = (exam1_grade + exam2_grade + exam3_grade) / 3

# Printing the average grade
print("The average grade is:", average_grade)
